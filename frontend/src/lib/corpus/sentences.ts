export function getRandomSentence(category?: string): CorpusSentence {
  let pool = CORPUS_SENTENCES;
  if (category && category !== 'all') {
    pool = pool.filter(s => s.category === category);
  }
  if (pool.length === 0) return CORPUS_SENTENCES[0];
  const index = Math.floor(Math.random() * pool.length);
  return pool[index];
}

export function getSentencesByCategory(category: string): CorpusSentence[] {
  if (!category || category === 'all') return CORPUS_SENTENCES;
  return CORPUS_SENTENCES.filter(s => s.category === category);
}

export function getAllCategories(): string[] {
  return Array.from(new Set(CORPUS_SENTENCES.map(s => s.category)));
}
